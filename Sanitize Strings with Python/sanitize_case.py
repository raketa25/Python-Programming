"""
This program demonstrates how to sanitize strings by changing their case. It takes a name, prints it in its original form, then converts it to lowercase, uppercase, and title case, printing the result at each step.  

"""
name = 'BoB'
print(name)

name = name.lower()
print(name)

name = name.upper()
print(name)

name = name.title()
print(name)